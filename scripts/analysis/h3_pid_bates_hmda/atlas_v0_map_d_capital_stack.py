"""
atlas_v0_map_d_capital_stack.py
================================
Displacement Defense Atlas v0 — Map (d), Five-Layer Capital Stack Composite
Dallas County, TX | Thesis: "Below the Line" | Nicholas Donovan Hawkins, TSU

ANALYTICAL PURPOSE
------------------
Map D is the V5 thesis's signature spatial argument: a six-panel 300 DPI
publication figure that renders each of the five capital-stack layers as a
stand-alone choropleth (A–E) and then composites them into a single
`capital_stack_score` choropleth (F). The composite operationalizes the
claim from `docs/theory/five_layer_capital_stack.md` that Dallas's public-
finance architecture is vertically integrated — that the layers compound.
Panels (A)–(E) allow visual verification that the composite is not driven
by any one layer alone. Panel (F) is the headline map for Chapter 4.

PANEL LAYOUT (2 rows × 3 columns)
---------------------------------
    (A) Layer 1 — CIP per capita (2012–present bond projects spatial-joined)
    (B) Layer 2 — PID annual budget per capita
    (C) Layer 3 — TIF / Opportunity Zone presence (binary any)
    (D) Layer 4 — Institutional-pressure proxy (real 10-yr gentrification
        or demographic-change signal; DCAD SFR pipeline pending)
    (E) Layer 5 — Vendor economic residue (5 mi vendor-capture share)
    (F) Composite — capital_stack_score (mean of A–E, each normalized 0–1)

INPUTS
------
    outputs/tables/capital_stack_inputs_v1.csv   (generated by this script
                                                 if missing; 645 tracts)
    outputs/geojson/tracts_pid_join.geojson      (Dallas County tract geometry)

OUTPUTS
-------
    maps/v0/atlas_map_d_capital_stack.png             (300 DPI)
    outputs/figures/atlas_v0_map_d_capital_stack.png  (300 DPI copy)

USAGE
-----
    python scripts/analysis/h3_pid_bates_hmda/atlas_v0_map_d_capital_stack.py

LAYER 4 CAVEAT
--------------
The Layer 4 choropleth uses a demographic-change proxy (real_gent_signal OR
demo_change_real from `h6_bates_full_typology.csv`). The canonical Layer 4
variable `sfr_investor_share` will be supplied when the DCAD institutional
ownership pipeline (`scripts/analysis/h4_sfr_institutional/
h4_sfr_institutional_flag.py`) has completed. This script reads from the
composite input file so the swap is one-line when DCAD output lands.
"""

