def add_north_arrow(ax, x: float = 0.94, y: float = 0.94, fontsize: int = 9) -> None:
    ax.annotate(
        "N\n▲",
        xy=(x, y), xycoords="axes fraction",
        ha="center", va="center",
        fontsize=fontsize, fontweight="bold", fontfamily="DejaVu Sans",
        bbox=dict(boxstyle="round,pad=0.2", facecolor="white",
                  edgecolor="#444444", linewidth=0.6, alpha=0.92),
    )


def add_scalebar(ax, length_m: float = 8047, label: str = "5 mi",
                 x0_frac: float = 0.05, y0_frac: float = 0.06) -> None:
    xlim = ax.get_xlim()
    ylim = ax.get_ylim()
    map_width_m  = xlim[1] - xlim[0]
    map_height_m = ylim[1] - ylim[0]
    x0 = xlim[0] + x0_frac * map_width_m
    y0 = ylim[0] + y0_frac * map_height_m
    ax.plot([x0, x0 + length_m], [y0, y0],
            color="black", linewidth=1.6, solid_capstyle="butt", zorder=7)
    for xt in (x0, x0 + length_m):
        ax.plot([xt, xt],
                [y0 - map_height_m * 0.005, y0 + map_height_m * 0.005],
                color="black", linewidth=1.2, zorder=7)
    ax.text(x0 + length_m / 2, y0 + map_height_m * 0.014,
            label, ha="center", va="bottom",
            fontsize=7, fontfamily="DejaVu Sans", zorder=7)


