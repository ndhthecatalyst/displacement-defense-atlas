def render_panel(ax, gdf_wm: gpd.GeoDataFrame, spec: dict,
                 i30_wm: gpd.GeoDataFrame,
                 county_boundary: gpd.GeoDataFrame) -> None:
    """Render a single capital-stack layer panel."""
    col = spec["key"]
    cmap = plt.get_cmap(spec["cmap"])

    if spec.get("binary"):
        # Two-class fill
        for val, color in [(0, "#f0f0f0"), (1, cmap(0.75))]:
            mask = gdf_wm[col] == val
            if mask.sum() > 0:
                gdf_wm[mask].plot(
                    ax=ax, facecolor=color, edgecolor="#aaaaaa",
                    linewidth=0.2, zorder=2,
                )
    else:
        gdf_wm.plot(
            ax=ax, column=col, cmap=cmap, vmin=0, vmax=1,
            edgecolor="#cccccc", linewidth=0.2, zorder=2,
        )

    # County outline + I-30
    county_boundary.plot(
        ax=ax, facecolor="none", edgecolor="#222222",
        linewidth=1.0, zorder=5,
    )
    i30_wm.plot(
        ax=ax, color="black", linestyle="--", linewidth=1.4, zorder=6,
    )

    # Extent
    bounds = gdf_wm.total_bounds
    pad_x = (bounds[2] - bounds[0]) * 0.04
    pad_y = (bounds[3] - bounds[1]) * 0.04
    ax.set_xlim(bounds[0] - pad_x, bounds[2] + pad_x)
    ax.set_ylim(bounds[1] - pad_y, bounds[3] + pad_y)
    ax.set_aspect("equal")
    ax.set_axis_off()

    add_north_arrow(ax)
    add_scalebar(ax)

    # Emphasize composite panel with thicker border
    if spec.get("composite"):
        for side in ("top", "right", "bottom", "left"):
            ax.spines[side].set_visible(False)
        # Panel outer highlight rectangle
        rect = mpatches.Rectangle(
            (0.002, 0.002), 0.996, 0.996, transform=ax.transAxes,
            fill=False, edgecolor="#7f0000", linewidth=2.2, zorder=10,
        )
        ax.add_patch(rect)

    # Panel title (letter + title + subtitle)
    panel_title = f"{spec['letter']}.  {spec['title']}"
    ax.set_title(
        f"{panel_title}\n{spec['subtitle']}",
        fontsize=11, fontweight="bold", fontfamily="DejaVu Sans",
        loc="left", pad=6,
    )

    # Colorbar / legend
    if spec.get("binary"):
        handles = [
            mpatches.Patch(facecolor="#f0f0f0", edgecolor="#aaaaaa",
                           label="Absent"),
            mpatches.Patch(facecolor=cmap(0.75), edgecolor="#aaaaaa",
                           label="Present"),
        ]
        leg = ax.legend(
            handles=handles, loc="lower right",
            fontsize=7.5, frameon=True, framealpha=0.92,
            edgecolor="#aaaaaa", fancybox=False,
        )
    else:
        # Compact inset colorbar
        sm = ScalarMappable(cmap=cmap, norm=mcolors.Normalize(vmin=0, vmax=1))
        sm.set_array([])
        cax = ax.inset_axes([0.72, 0.08, 0.22, 0.025])
        cb = plt.colorbar(sm, cax=cax, orientation="horizontal")
        cb.ax.tick_params(labelsize=6, length=2)
        cb.set_label("normalized (0–1)", fontsize=6.5,
                     fontfamily="DejaVu Sans", labelpad=2)


# ---------------------------------------------------------------------------
# Main builder
# ---------------------------------------------------------------------------

def build_figure() -> None:
    df = build_inputs()
    print(f"[Map D] Inputs: {len(df)} tracts")

    # Join geometry
    tracts = gpd.read_file(TRACT_GEO)
    tracts["GEOID"] = tracts["GEOID"].astype(str).str.zfill(11)
    df["GEOID"] = df["GEOID"].astype(str).str.zfill(11)
    gdf = tracts.merge(df, on="GEOID", how="left").to_crs(epsg=3857)

    # Shared geometry
    i30_wm = gpd.GeoDataFrame(
        geometry=[LineString(I30_COORDS_WGS84)], crs="EPSG:4326"
    ).to_crs(epsg=3857)
    county_boundary = gdf.dissolve()

    # 2×3 grid
    fig, axes = plt.subplots(
        2, 3, figsize=(FIG_WIDTH, FIG_HEIGHT), dpi=DPI,
        gridspec_kw=dict(wspace=0.02, hspace=0.18),
    )

    for ax, spec in zip(axes.flatten(), LAYER_SPECS):
        render_panel(ax, gdf, spec, i30_wm, county_boundary)

    # Suptitle + subtitle
    fig.suptitle(SUPTITLE, fontsize=17, fontweight="bold",
                 fontfamily="DejaVu Sans", y=0.985)
    fig.text(
        0.5, 0.955, SUBTITLE,
        fontsize=11, fontstyle="italic", fontfamily="DejaVu Sans",
        color="#444444", ha="center",
    )

    # Footnote
    fig.text(
        0.01, 0.008, FOOTNOTE,
        fontsize=7.4, fontfamily="DejaVu Sans",
        color="#555555", ha="left", va="bottom", wrap=True,
    )

    # Save
    OUT_PRIMARY.parent.mkdir(parents=True, exist_ok=True)
    OUT_SECONDARY.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(OUT_PRIMARY, dpi=DPI, bbox_inches="tight",
                facecolor="white", edgecolor="none")
    fig.savefig(OUT_SECONDARY, dpi=DPI, bbox_inches="tight",
                facecolor="white", edgecolor="none")
    plt.close(fig)

    # Diagnostics
    print(f"[Map D] Saved → {OUT_PRIMARY.relative_to(REPO_ROOT)}")
    print(f"[Map D] Saved → {OUT_SECONDARY.relative_to(REPO_ROOT)}")
    print("\n[Map D] capital_stack_score — North vs South of I-30:")
    grp = df.groupby("south_of_i30")["capital_stack_score"].agg(["mean","std","count"])
    print(grp)
    print("\n[Map D] Bottom quartile (stacked disinvestment):")
    q25 = df["capital_stack_score"].quantile(0.25)
    bq = df[df["capital_stack_score"] <= q25]
    print(f"  n={len(bq)}; south_of_i30 share = "
          f"{(bq['south_of_i30']==1).mean()*100:.1f}%")


if __name__ == "__main__":
    build_figure()
